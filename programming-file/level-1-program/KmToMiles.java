import java.util.*;
// Program to convert kilometers to miles
class KmToMiles {
    public static void main(String[] args) {

        // Declare distance in kilometers
        double km = 10.8;

        // Convert to miles
        double miles = km / 1.6;

        // Display result
        System.out.println("The distance " + km + " km in miles is " + miles);
    }
}
