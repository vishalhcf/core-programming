import java.util.*;
// Program to calculate the age of Harry in 2024
// Program to calculate the age of Harry in 2024
class CalculateAge {
    public static void main(String[] args) {

        // Declare and assign birth year and current year
        int birthYear = 2000;
        int currentYear = 2024;

        // Calculate age
        int age = currentYear - birthYear;

        // Display the result
        System.out.println("Harry's age in " + currentYear + " is " + age);
    }
}

